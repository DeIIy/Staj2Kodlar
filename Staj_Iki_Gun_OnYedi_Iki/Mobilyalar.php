        <?php
        // Veritabanı bağlantısı oluşturun
        $host = "localhost";
        $username = "root";
        $password = "";
        $database = "yusufticaret";

        $conn = new mysqli($host, $username, $password, $database);

        // Bağlantıyı kontrol edin
        if ($conn->connect_error) {
            die("Bağlantı hatası: " . $conn->connect_error);
        }

        if ($_SERVER["REQUEST_METHOD"] == "POST") {
            // Ekle butonuna tıklanırsa
            if (isset($_POST["urun_adi"], $_POST["degerlendirme"], $_POST["fiyat"])) {
                $urun_adi = $_POST["urun_adi"];
                $degerlendirme = $_POST["degerlendirme"];
                $fiyat = $_POST["fiyat"];
                
                // Sepet tablosuna ekleme yap
                $sql = "INSERT INTO sepet (urun_adi, degerlendirme, fiyat) VALUES ('$urun_adi', $degerlendirme, $fiyat)";
                
                if ($conn->query($sql) === TRUE) {
                    echo "Ürün sepete başarıyla eklendi";
                } else {
                    echo "Hata: " . $conn->error;
                }
            }
        }

        // Bağlantıyı kapat
        $conn->close();
